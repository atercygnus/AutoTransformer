    def display_mode_code_h(self, value):

        value = value & 0b11111111

        exhaust = value & 0b00000001
        mode = value & 0b00000011 - 1

        di1 = (value & 0b00000100) >> 2
        di2 = (value & 0b00001000) >> 3
        di3 = (value & 0b00010000) >> 4
        di4 = (value & 0b00100000) >> 5
        force_mode = (value & 0b11000000) >> 6

        self.lcd_mode_code.display(value)

        self.bn_disable_cp.setEnabled(exhaust)
        self.bn_enable_cp.setEnabled(exhaust)
        self.bn_remote_cp.setEnabled(exhaust)
        self.cb_MODE_CODE_2.setEnabled(exhaust)

        self.cb_MODE_CODE_1.setCurrentIndex(exhaust)
        self.cb_MODE_CODE_2.setCurrentIndex(mode)

        self.cb_mode_code_di1.setCurrentIndex(di1)
        self.cb_mode_code_di2.setCurrentIndex(di2)
        self.cb_mode_code_di3.setCurrentIndex(di3)
        self.cb_mode_code_di4.setCurrentIndex(di4)

        self.cb_MODE_CODE_FORCE_MODE.setCurrentIndex(force_mode)
        self.cb_force_mode_cp.setChecked(force_mode > 0)

        if exhaust == 0:
            self.rb_mode_1_cp.setChecked(True)
        else:
            self.rb_mode_2_cp.setChecked(True)

