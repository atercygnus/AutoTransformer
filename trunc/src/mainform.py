    def force_mode_immediately(self):
        if self.cb_force_mode_cp.isChecked():
            self.at['MODE_CODE_H'].value = self.at['MODE_CODE_H'].value | 0b11000000
        else:
            self.at['MODE_CODE_H'].value = self.at['MODE_CODE_H'].value & 0b00111111

    def set_disable(self):
        value = self.at['MODE_CODE_H'].value
        value = value | 0b00000001
        value = value & 0b11111101
        self.at['MODE_CODE_H'].value = value

    def set_enable(self):
        value = self.at['MODE_CODE_H'].value
        value = value | 0b00000010
        value = value & 0b11111110
        self.at['MODE_CODE_H'].value = value

    def set_remote(self):
        value = self.at['MODE_CODE_H'].value
        value = value | 0b00000011
        self.at['MODE_CODE_H'].value = value

